package datos;

/**
 * Interfaz que define las operaciones básicas de un CRUD (Crear, Leer, Actualizar, Eliminar).
 */
public interface InterfaceCRUD {
    /**
     * Método para crear un nuevo elemento.
     *
     * @return true si se crea correctamente, false en caso contrario.
     */
    public boolean Crear();
}
